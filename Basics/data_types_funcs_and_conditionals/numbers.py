#PEMDAS - order of evaluation of mathematical operations
# Parenthesis, Exponents, Multiplication, Division, Addition, Subtraction
# ** exponentiation operator

print(5 ** 5)
print(15 / 3)
print(14 / 3)  # returns float
print(14 // 3) # returns integer
print(14 % 3)  # remainder


print(int(3.14)) # returns 3 - can take string and other valid input types
print(float("5.43"))
print(str(9))
